<h>Beyond Academics</h>
<p>A students’ branch of the Institute of Electrical and Electronics Engineers, USA, is functioning in IIIT-K 
 with a motto to create an awareness on the budding Engineers on the latest in technological innovations.
 The student branch is a well organised, robust body having a separate office and library functioning in the 
 main building.The student branch take up a host of activities throughout the year including guest lectures,
 video screening, technical quizzes, mock-interviews and design contests for the benefit of students in
 Electronics and Computer science branches</p>
 <p>Industrial visits are also conducted for every batch to introduce students to professional field.An
 Entrepreneurship Development Cell is functioning in the college with the intention of nurturing
 entrepreneurship skills of the students. The cell provides a platform for the students to pursue
 entrepreneurial activities and also provide assistance to potential entrepreneurs. With the prime goal of
 developing responsible innovators out of engineers, the EDC strives to assist every aspiring entrepreneur
 on every single step. With active involvement of students the cell promotes and revitalizes the 
 entrepreneurial culture in the college.</p>

